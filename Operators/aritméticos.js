 // Atribuindo valores às variáveis x e y
let x = 10;
let y = 3;


// Operações matemáticas entre as variáveis e impressão no console
console.log(x+y);
console.log(x - y);
console.log(x * y);
console.log(x / y);
console.log(x % y);
console.log( x ** y);


// Incrementando o valor de x em 1 e depois imprimindo o valor de x
console.log(x++);
console.log(x);

// Decrementando o valor de x em 1 e depois imprimindo o valor de x
console.log(--x);

